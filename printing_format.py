#more info about this @ https://pyformat.info/  

#printing format stuff 

print("This is a {} ".format("test"))

#with variables 

print("My name is {name} ".format(name="Rick metal"))

#and with numbers 

print("My name is {name} and im {years} years ".format(name="Rick metal",years="90"))

#with floats

print("My name is {name} and im {years} years and my salary is {salary:4.3f} dollars".format(name="Rick metal",years="90",salary=7034.034567))